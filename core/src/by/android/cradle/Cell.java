package by.android.cradle;

public class Cell {
    private int Row;
    private int Col;

    public Cell(int row, int col){
        Row = row;
        Col = col;
    }
    public int getRow() {
        return Row;
    }

    public void setRow(int row) {
        Row = row;
    }

    public int getCol() {
        return Col;
    }

    public void setCol(int col) {
        Col = col;
    }



}
